import Foundation
import UIKit

extension UIColor {
    
    class var completeGreen: UIColor {
        return UIColor(red: 68.0 / 255.0, green: 219.0 / 255.0, blue: 94.0 / 255.0, alpha: 1.0)
    }
    
    class var contactBlue: UIColor {
        return UIColor(red: 41.0 / 255.0, green: 98.0 / 255.0, blue: 255.0 / 255.0, alpha: 1.0)
    }
    
}


